#!/bin/bash

# Βρίσκει όλα τα αρχεία .sh στον τρέχοντα κατάλογο που βρίσκεται ο user 
for file in *.sh
do
  # Ελέγχει αν βρέθηκε αρχείο (για να μη τυπώσει για το *.sh αν δεν υπάρχει κανένα δηλαδή)
  if [ -f "$file" ]; then
    echo "Βρέθηκε αρχείο: $file."
    echo "Τα περιεχόμενα του αρχείου είναι:"
    cat "$file"
    echo "----------------------------------------------"
  fi
done 
